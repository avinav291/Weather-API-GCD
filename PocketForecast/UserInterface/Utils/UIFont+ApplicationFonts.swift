

import Foundation

extension UIFont {
    
    public class func applicationFontOfSize(size : CGFloat) -> UIFont {
        return UIFont(name: "Varela Round", size: size)!
    }
    
    public class func temperatureFontOfSize(size : CGFloat) -> UIFont {
        return UIFont(name: "Questrial", size: size)!
    }
        
}
